class Even
{
    public static void main(String[] args) {
        
        int no=2;
        {
            System.out.println("Number is "+no);
            if(no%2 == 0)
            {
                System.out.println("Number is Even");
            }
            else
            {
                System.out.println("Number is Odd");
            }
        }
    }
    
}